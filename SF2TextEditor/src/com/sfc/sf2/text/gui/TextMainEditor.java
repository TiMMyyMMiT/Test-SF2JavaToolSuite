    @Override
    protected void initEditor() {
        super.initEditor();
        
        accordionPanel2.setExpanded(false);
        
        sorter = new TableRowSorter<>(textTableModel);
        tableText.jTable.setRowSorter(sorter);
        tableText.addListSelectionListener(this::onSelectionChanged);
        tableText.addTableModelListener(this::onTableDataChanged);
        tableText.jTable.addPropertyChangeListener("tableCellEditor", this::onTableEditorStarted);
        TableColumnModel columns = tableText.jTable.getColumnModel();
        columns.getColumn(0).setMaxWidth(50);
        columns.getColumn(1).setMaxWidth(50);
        
        ListenersHelpers.addTextFieldChangeListener(jTextFieldFilter, this::onFilterChanged);
    }
    
    @Override
    protected void onDataLoaded() {
        super.onDataLoaded();
        textPreviewLayoutPanel.setBaseTiles(textManager.getBaseTiles());
        textPreviewLayoutPanel.setFontSymbols(textManager.getFontSymbols());
        textPreviewLayoutPanel.setAllyNames(textManager.getAllyNames());
        ActionManager.setAndExecuteAction(new NonCombinableAction<String[]>(this, "Game Script Imported", this::actionScriptLoaded, textManager.getGameScript(), textTableModel.getTableData(String[].class)));
    }
    
    private void actionScriptLoaded(String[] script) {
        textTableModel.setTableData(script);
    }

