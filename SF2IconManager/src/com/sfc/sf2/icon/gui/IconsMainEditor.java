    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        Path graphicPath = PathHelpers.getBasePath().resolve(directoryButton3.getDirectoryPath());
        if (!PathHelpers.createPathIfRequred(graphicPath)) return;
        try {
            iconManager.exportAllDisassemblies(graphicPath, iconsLayoutPanel.getIcons());
        } catch (Exception ex) {
            Console.logger().log(Level.SEVERE, null, ex);
            Console.logger().severe("ERROR Icons disasm could not be exported to : " + graphicPath);
        }
    }//GEN-LAST:event_jButton2ActionPerformed

    private void jButton13ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton13ActionPerformed
        Path graphicPath = PathHelpers.getBasePath().resolve(directoryButton4.getDirectoryPath());
        if (!PathHelpers.createPathIfRequred(graphicPath)) return;
        try {
            IconExportMode exportMode = iconSettings.getExportMode();
            FileFormat format = jRadioButton1.isSelected() ? FileFormat.PNG : FileFormat.GIF;
            int scale = (int)viewPanel1.getItemsPerRowSpinner().getValue();
            iconManager.exportAllImages(graphicPath, iconsLayoutPanel.getIcons(), exportMode, scale, format);
        } catch (Exception ex) {
            Console.logger().log(Level.SEVERE, null, ex);
            Console.logger().severe("ERROR Icons images could not be exported to : " + graphicPath);
        }
    }//GEN-LAST:event_jButton13ActionPerformed

    private void jButton12ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton12ActionPerformed
        Path palettePath = PathHelpers.getBasePath().resolve(fileButton1.getFilePath());
        Path graphicPath = PathHelpers.getBasePath().resolve(directoryButton2.getDirectoryPath());
        try {
            FileFormat format = FileFormat.ANY_IMAGE;
            iconManager.importAllImages(palettePath, graphicPath, format);
        } catch (Exception ex) {
            iconManager.clearData();
            Console.logger().log(Level.SEVERE, null, ex);
            Console.logger().severe("ERROR Icons images could not be imported from : " + graphicPath);
        }
        onDataLoaded();
    }//GEN-LAST:event_jButton12ActionPerformed

