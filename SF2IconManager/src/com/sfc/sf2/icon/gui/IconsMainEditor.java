    @Override
    protected void initEditor() {
        super.initEditor();
        
        viewPanel1.setLayoutPanel(iconsLayoutPanel, viewSettings);
        
        jComboBox2.removeAllItems();
        IconExportMode mode = iconSettings.getExportMode();
        IconExportMode[] exportModes = IconExportMode.values();
        for (int i = 0; i < exportModes.length; i++) {
            jComboBox2.addItem(exportModes[i].toString());
        }
        jComboBox2.setSelectedItem(mode.toString());
        jRadioButton1.setSelected(iconSettings.getExportFileFormat() == FileFormat.PNG);
        jRadioButton2.setSelected(iconSettings.getExportFileFormat() != FileFormat.PNG);
    }
    
    @Override
    protected void onDataLoaded() {
        super.onDataLoaded();
        ActionManager.setAndExecuteAction(new NonCombinableAction<Icon[]>(this, "Icons Imported", this::actionIconsLoaded, iconManager.getIcons(), iconsLayoutPanel.getIcons()));
    }
    
    private void actionIconsLoaded(Icon[] icons) {
        iconsLayoutPanel.setIcons(icons);
        if (icons != null && icons.length > 0) {
            iconsLayoutPanel.setItemsPerRow((int)viewPanel1.getItemsPerRowSpinner().getValue());
        }
    }
    
