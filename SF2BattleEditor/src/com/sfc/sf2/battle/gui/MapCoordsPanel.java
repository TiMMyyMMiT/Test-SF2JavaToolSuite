    private void jSpinnerMapIndexStateChanged(javax.swing.event.ChangeEvent evt) {//GEN-FIRST:event_jSpinnerMapIndexStateChanged
        if (coords == null) return;
        if (!ActionManager.isActionTriggering()) {
            ActionManager.setActionWithoutExecute(new SpinnerAction(jSpinnerMapIndex, jSpinnerMapIndex.getValue(), coords.getMapIndex()));
        }
        int newMapIndex = (int)jSpinnerMapIndex.getModel().getValue();
        if (newMapIndex != coords.getMapIndex()) {
            if (mapIndexChangedListener != null) {
                mapIndexChangedListener.actionPerformed(new ActionEvent(jSpinnerMapIndex, newMapIndex, "Change Map"));
            }
        }
    }//GEN-LAST:event_jSpinnerMapIndexStateChanged

