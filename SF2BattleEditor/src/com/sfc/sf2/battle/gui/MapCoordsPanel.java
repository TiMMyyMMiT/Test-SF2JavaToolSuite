    private void jSpinnerStartYStateChanged(javax.swing.event.ChangeEvent evt) {//GEN-FIRST:event_jSpinnerStartYStateChanged
        if (coords == null) return;
        if (!ActionManager.isActionTriggering()) {
            ActionManager.setActionWithoutExecute(new SpinnerAction(jSpinnerStartY, jSpinnerStartY.getValue(), coords.getY()));
        }
        int newVal = (int)jSpinnerStartY.getModel().getValue();
        if (jCheckBoxLockPositions.isSelected()) {
            int yShift = coords.getY() - newVal;
            layoutPanel.getBattle().getSpriteset().shiftPositions(0, yShift);
        }
        coords.setY(newVal);
        layoutPanel.redraw();
    }//GEN-LAST:event_jSpinnerStartYStateChanged

    private void jSpinnerTriggerYStateChanged(javax.swing.event.ChangeEvent evt) {//GEN-FIRST:event_jSpinnerTriggerYStateChanged
        if (coords == null) return;
        if (!ActionManager.isActionTriggering()) {
            ActionManager.setActionWithoutExecute(new SpinnerAction(jSpinnerTriggerY, jSpinnerTriggerY.getValue(), coords.getTrigY()));
        }
        int oldVal = coords.getTrigY();
        int newVal = (int)jSpinnerTriggerY.getModel().getValue();
        if (oldVal < newVal && newVal >= 64) {
            newVal = 255;
            jSpinnerTriggerY.getModel().setValue(newVal);
        } else if (oldVal > newVal && newVal >= 64) {
            newVal = 63;
            jSpinnerTriggerY.getModel().setValue(newVal);
        }
        coords.setTrigY(newVal);
        layoutPanel.redraw();
    }//GEN-LAST:event_jSpinnerTriggerYStateChanged

    private void jSpinnerStartXStateChanged(javax.swing.event.ChangeEvent evt) {//GEN-FIRST:event_jSpinnerStartXStateChanged
        if (coords == null) return;
        if (!ActionManager.isActionTriggering()) {
            ActionManager.setActionWithoutExecute(new SpinnerAction(jSpinnerStartX, jSpinnerStartX.getValue(), coords.getX()));
        }
        int newVal = (int)jSpinnerStartX.getModel().getValue();
        BattleMapCoords coords = layoutPanel.getBattle().getBattleCoords();
        if (jCheckBoxLockPositions.isSelected()) {
            int xShift = coords.getX() - newVal;
            layoutPanel.getBattle().getSpriteset().shiftPositions(xShift, 0);
        }
        coords.setX(newVal);
        layoutPanel.redraw();
    }//GEN-LAST:event_jSpinnerStartXStateChanged

